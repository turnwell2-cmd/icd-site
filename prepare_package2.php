      <p align="justify"><font size="2" face="Arial, Helvetica, sans-serif"><strong>Inflatable 
        packaging (air bags)</strong><br>
        Used primarily as void-fill materials for lightweight items<br>
        Not recommended for items with sharp corners or edges<br>
        Extreme hot or cold temperatures may affect the ability of air bags to 
        provide adequate product protection.</font></p>
      <p align="justify"><font size="2" face="Arial, Helvetica, sans-serif"><strong>Expanded 
        polystyrene &quot;peanuts&quot;</strong><br>
        Used primarily as void-fill material for lightweight items<br>
        Overfill the box with peanuts, gently close the flaps, and seal securely<br>
        Do not use with flat or narrow products that may move to the edge or bottom 
        of the carton in transit<br>
        Due to the shifting and settling properties of peanuts, it is recommended 
        that a minimum of three inches of cushioning be used around the contents</font></p>
      <p align="justify"><font size="2" face="Arial, Helvetica, sans-serif"><strong>Engineered 
        foam enclosures</strong><br>
        Materials may include expanded polystyrene, polyethylene, polypropylene, 
        or copolymers<br>
        Enclosures should be pre-engineered for specific products</font></p>
      <p align="justify"><font size="2" face="Arial, Helvetica, sans-serif"><strong>Foam-in-place</strong> 
        (a foam, sprayed into the box or mixed in packets, that expands and forms 
        a protective mold around contents)<br>
        Must be properly used, with even foam distribution around the contents<br>
        Select appropriate density of foam to meet packaging needs, which can 
        range from void-fill applications to high-performance cushioning</font></p>
      <p align="justify"><font size="2" face="Arial, Helvetica, sans-serif"><strong>Corrugated 
        liners and inserts</strong><br>
        May be added to the package to increase strength and improve package performance</font></p>
      <p align="justify"><font size="2" face="Arial, Helvetica, sans-serif"><strong>Crumpled 
        kraft paper</strong><br>
        Used primarily as a void-fill material for light-to-medium weight, non-fragile 
        items and items that are suitable for such packing materials<br>
        Must be tightly crumpled<br>
        Place at least four in. (10 cm.) of paper between contents and outer box</font></p>
