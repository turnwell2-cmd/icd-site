    <td height="44"></td>
    <td colspan="4" rowspan="2" valign="top"><div align="justify"><font size="2" face="Arial, Helvetica, sans-serif">Yes, 
        it's true: the devil is in the details. Sellers need to ensure that their 
        merchandise arrives intact. With that in mind:<br>
        Use a corrugated box.<br>
        Be aware that boxes usually have the maximum weight they will withstand 
        printed on one of the flaps--make sure your merchandise doesn't exceed 
        that amount.<br>
        Use a box that is the proper size for the object you are packing.<br>
        Make sure the carton doesn't exceed the shipper's maximum size or weight 
        (rules vary) .<br>
        Place enough packing material above, beneath, and between objects to keep 
        them from touching the insides of the carton or each other.<br>
        If packing multiple objects in the same box, wrap them individually with 
        bubble wrap, foam, or layers of tissue or newspaper; and bind snugly (but 
        not too tight!) with masking or other tape.<br>
        If objects are extra fragile, pack them, wrapped as above, in individual 
        boxes within the larger carton.<br>
        Use packing peanuts for lightweight objects that won't shift during handling.<br>
        When you pack a carton with multiple pieces, write the total number prominently 
        on the top, then number each piece within: &quot;1 of 3,&quot; &quot;2 
        of 3,&quot; and so on, so nothing gets left behind in the peanuts.<br>
        Use at least two strips around the carton to reinforce it (other than 
        the tape used for building and sealing it).</font> </div></td>
