        Brown paper tape in 60-pound grade, which comes as either self-adhesive 
        or ready for you to moisten and smooth down. (Paper tape works very well 
        for reinforcing beat-up boxes.)<br>
        Filament tape, an almost indestructible product for sealing heavier cartons.<br>
        Masking tape, which isn't suitable for sealing boxes but is great for 
        wrapping and securing fragile pieces; some art packers use masking tape 
        criss-crossed directly onto the glass on framed works of art, to minimize 
        damage if the glass breaks. (Unpackers: use orange oil or Goo-off to remove 
        the traces of glue left behind on glass.)</font></p></td>
