    <td colspan="4" valign="top"><div align="justify"><font size="2" face="Arial, Helvetica, sans-serif">As 
        we learned in high school, whether it's fair or not, a good-looking package 
        will be treated more kindly by those who encounter it. To handlers and 
        recipients alike, sloppy wrapping and labeling bespeak insignificant contents. 
        Neatness and proper documentation make sellers look professional. Here 
        are some pointers:<br>
        Seal packages carefully, tape with straight lines, and cut the tape cleanly 
        (no biting!).<br>
        Some Transaction place a transaction record or packing slip in an envelope, 
        which, taped to the top of the box, doubles as an address label.<br>
        Include the ship-to address on a label or slip of paper inside the package.<br>
        Create neat, clearly legible address labels.</font></div></td>
