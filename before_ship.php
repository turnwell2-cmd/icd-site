    <td></td>
    <td colspan="4" valign="top"><div align="justify"><font size="2" face="Arial, Helvetica, sans-serif">How 
        do you get packing supplies without spending a fortune? Easy. If you ship 
        regularly, hoarding and reusing materials can save you considerable expense. 
        Though we advise using a new carton, we take this to mean a sturdy one, 
        with flaps, that will withstand reasonable pressure. Here are some ideas: 
        </font> </div>
      <p align="justify"><font color="#999999" size="2" face="Arial, Helvetica, sans-serif">Ask 
        friends, your local market, and the Transaction department at work to save 
        boxes and packing materials for you.<br>
        Save space by breaking down boxes and storing them flat. Capture Styrofoam 
        packing peanuts in plastic trash bags.<br>
        When a sturdy padded envelope crosses your path, peel off the old label 
        (carefully, or you'll be covered in grey stuffing) and use it for Transaction 
        books or other flat objects.<br>
        Bubble wrap, too, can be reused (if only for the valium-like effect of 
        popping it between your fingers).<br>
        Save cardboard mailing tubes for posters or unmounted works on paper.<br>
        Keep old newspapers; some packers swear by it--whether it's balled-up 
        or shredded--as the best packing material around.</font></p></td>
