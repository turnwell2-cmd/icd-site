    <td colspan="4" valign="top"><font size="2" face="Arial, Helvetica, sans-serif"> 
      <div align="justify">Of course, tape is a packing essential. But there are 
        many varieties of tape you should know about, including: </div>
      </font> <p align="justify"><font color="#999999" size="2" face="Arial, Helvetica, sans-serif">Wide 
